#include "libft.h"

size_t	ft_strlcpy(char *dest, const char *src, size_t size)
{
	size_t i;
	size_t temp;

	i = 0;
	temp = ft_strlen(src);
	if (!dest || !src || size == 0)
		return (0);
	while (i < temp && i + 1 < size)
	{
		dest[i] = src[i];
		i++;
	}
	if (size > 0)
		dest[i] = '\0';
	return (temp);
}
